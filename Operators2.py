# Comparison Operators
x = 5
y = 7
print(x == y)
print(x != y)
print(x <= y)
print(x >= y)
print(x < y)
print(x > y) 

# Logical Operators
a = 5
b = 7
c = "abcefghijkm"
print(a == b and a != c)
print(a == b or a != c)
print(not a == c)

# Identity Operator
d = [1,2,3]
e = [4,5,6]
print(d is e)
print(d is not e)

# Membership Operator
f = [1,4,5,9]
print(4 in f)
print(8 not in f)
